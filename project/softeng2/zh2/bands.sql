    BandID INT IDENTITY(1,1) PRIMARY KEY,
    Name VARCHAR(100) NOT NULL,
    Genre VARCHAR(50) NOT NULL,
    FormationYear INT NOT NULL,
    IsActive BIT NOT NULL,
    Country VARCHAR(50) NOT NULL
);

